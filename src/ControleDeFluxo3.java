public class ControleDeFluxo3 {
    public static void main(String[] args) {
        //DO WHILE estrutura de repetição
        int contador = 0;
        while (contador <= 10){
            contador ++ ;
            System.out.println("contador funcionando");
        }
        // estrutura FOR
        for (int i = 0; i<=100; i++)
            System.out.println("oi");
    }
}
